def plot_ntd_panel(title: str,
                   close: pd.Series,
                   window: int,
                   shade: bool,
                   show_npx: bool,
                   mark_cross: bool,
                   sr_sup: pd.Series = None,
                   sr_res: pd.Series = None,
                   show_channel: bool = True):
    """
    NTD panel with optional NPX overlay and optional channel highlight.
    """
    c = _coerce_1d_series(close)
    ntd = compute_normalized_trend(c, window=int(window))
    npx = compute_normalized_price(c, window=int(window))

    fig, ax = plt.subplots(figsize=(12.5, 3.7))
    ax.set_title(title)

    ax.plot(ntd.index, ntd.values, label="NTD", linewidth=1.8)
    ax.axhline(0.0, linewidth=1.0, alpha=0.35)

    if shade:
        shade_ntd_regions(ax, ntd)

    if show_npx:
        ax.plot(npx.index, npx.values, label="NPX", linewidth=1.3, alpha=0.8)
        if mark_cross:
            up, dn = _cross_series(npx, pd.Series(0.0, index=npx.index))
            if up.any():
                ax.scatter(up[up].index, npx.loc[up[up].index], s=40, marker="o", alpha=0.8, label="NPX cross up")
            if dn.any():
                ax.scatter(dn[dn].index, npx.loc[dn[dn].index], s=40, marker="o", alpha=0.8, label="NPX cross down")

    # Reversal stars (NTD direction change, confirmed by consecutive bars)
    rb = max(1, int(rev_bars_confirm))
    ntd_s = ntd.dropna()
    if len(ntd_s) >= rb + 2:
        sign = np.sign(ntd_s)
        flip = (sign != sign.shift(1)) & (sign != 0) & (sign.shift(1) != 0)
        # Confirm flip by rb consecutive bars in new sign
        for t in flip[flip].index:
            loc = ntd_s.index.get_loc(t)
            seg = sign.iloc[loc:loc+rb]
            if len(seg) == rb and np.all(seg == seg.iloc[0]):
                ax.scatter([t], [float(ntd_s.loc[t])], marker="*", s=160, alpha=0.9, label="Reversal (confirmed)")

    # Channel highlight when price is between SR (on NTD panel)
    if show_channel and (sr_sup is not None) and (sr_res is not None):
        sup = _coerce_1d_series(sr_sup).reindex(c.index).ffill()
        res = _coerce_1d_series(sr_res).reindex(c.index).ffill()
        in_chan = (c >= sup) & (c <= res)
        if in_chan.any():
            # shade only contiguous regions for readability
            in_chan = in_chan.reindex(ntd.index).fillna(False)
            blocks = []
            start = None
            for t, v in in_chan.items():
                if v and start is None:
                    start = t
                if (not v) and start is not None:
                    blocks.append((start, t))
                    start = None
            if start is not None:
                blocks.append((start, in_chan.index[-1]))
            for a, b in blocks[-12:]:
                ax.axvspan(a, b, alpha=0.05, color="tab:purple", zorder=0)

    style_axes(ax)
    ax.set_ylim(-1.05, 1.05)
    ax.legend(loc="upper left", ncols=3)
    fig.tight_layout()
    return fig, {"ntd": ntd, "npx": npx}

