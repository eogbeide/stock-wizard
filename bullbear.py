def bull_bear_index(close: pd.Series, lookback: str = "6mo"):
    """
    0..1 index: position of last close within (min,max) of lookback period.
    """
    c = _coerce_1d_series(close).dropna()
    if c.empty:
        return float("nan"), float("nan"), float("nan")
    end = c.index.max()
    if lookback == "1mo":
        start = end - pd.Timedelta(days=30)
    elif lookback == "3mo":
        start = end - pd.Timedelta(days=90)
    elif lookback == "6mo":
        start = end - pd.Timedelta(days=182)
    else:
        start = end - pd.Timedelta(days=365)
    w = c.loc[(c.index >= start) & (c.index <= end)]
    if w.empty:
        w = c
    lo, hi = float(w.min()), float(w.max())
    last = float(w.iloc[-1])
    if not np.isfinite(lo) or not np.isfinite(hi) or hi == lo:
        return float("nan"), lo, hi
    idx = (last - lo) / (hi - lo)
    return float(np.clip(idx, 0.0, 1.0)), lo, hi


